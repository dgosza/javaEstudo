public class TiposPrimitivos {
    public static void main(String[] args) {


        // BOOLEAN - TRUE OR FALSE
        boolean verifica = false;


        // CHAR - UNICODE
        char name = 'd';
        char letter = '\u0041';


        // BYTE - MAXIMO: 127  MINIMO: 128
        byte x = 127;


        //SHORT - MAXIMO: 32767 MINIMO: 32768
        short number1 = 32767;
        short number2 = -32768;


        // INT - MAXIMO: 2147483647 MINIMO: 2147483648
        int maximo = 2147483647;
        int minimo = -2147483648;


        // LONG - Sempre que declarar uma varial do tipo LONG, colocar o L no final
        long longNumber = 2147483648L;


        // FLOAT E DOUBLE
        float n1 = 4.5f;
        double n2 = 4.5;


        String nome = "Diego Souza";
        Object objeto = "Diego";
        Object objeto2 = 4.5f;
        Object objeto3 = 432423423523234235L;



    }
}
