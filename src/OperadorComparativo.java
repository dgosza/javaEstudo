public class OperadorComparativo {

    public static void main(String[] args) {


        int a = 10;


        boolean c5 = a <= 10;
        boolean c6 = a >= 10;
        boolean c7 = a == 10;
        boolean c8 = a != 10;

        System.out.println(c5);
        System.out.println(c6);
        System.out.println(c7);
        System.out.println(c8);



    }
}
